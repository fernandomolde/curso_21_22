"""
Mono del ejercicio:

cadena de texto es un (STR)

Para convertir cada letra a su codigo ASCII es con ORD o CHR y se llamara total_1

cuando tengamos ya el codigo ASCII cuando haya un numero 7 se cambie por 1 y ese numero se llamara total_2

y por ultimo delvolvemos total_1 - total_2

"""

def code(datos_A):
    total_1 = []
    for i in datos_A:
        total_1.append(ord(i))
    return code

code(1)
